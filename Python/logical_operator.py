# Logical operation which takes user input

x = float(input("Enter value of x:- "))
y = float(input("Enter value of y:- "))

# AND Operator
# If x is False, it returns x, otherwise it returns y
print("AND Operation:- ", x and y)

# OR Operator
# If x is False, it returns y, otherwise it returns x
print("OR Operation:- ", x or y)

# NOT Operator
# If x is False, it returns True, otherwise True
print("NOT Operation:- ", not x)


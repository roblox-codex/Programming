# Unary minus (-) operator
num1 = 10
num2 = -num1
print("Unary Minus Operator:", num2)

# Unary plus (+) operator (not very common, as it doesn't change the value)
num3 = +num1
print("Unary Plus Operator:", num3)

# Bitwise NOT (~) operator
bit_value = 0b101010
bit_complement = ~bit_value
print("Bitwise NOT Operator:", bin(bit_complement))

# Logical NOT (not) operator
bool_value = True
bool_negated = not bool_value
print("Logical NOT Operator:", bool_negated)

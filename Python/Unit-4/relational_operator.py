# Relational Operators Program

# Input two numbers from the user
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Greater than (>)
greater_than_result = num1 > num2
print(f"{num1} > {num2}:", greater_than_result)

# Less than (<)
less_than_result = num1 < num2
print(f"{num1} < {num2}:", less_than_result)

# Greater than or equal to (>=)
greater_than_equal_result = num1 >= num2
print(f"{num1} >= {num2}:", greater_than_equal_result)

# Less than or equal to (<=)
less_than_equal_result = num1 <= num2
print(f"{num1} <= {num2}:", less_than_equal_result)

# Equal to (==)
equal_to_result = num1 == num2
print(f"{num1} == {num2}:", equal_to_result)

# Not equal to (!=)
not_equal_result = num1 != num2
print(f"{num1} != {num2}:", not_equal_result)

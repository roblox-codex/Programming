// 1-d Array

#include <iostream>

using namespace std;

int main() {
    const int size = 5;
    int arr[size] = {1, 2, 3, 4, 5};

    return 0;
}


// 2-d Array

#include <iostream>

using namespace std;

int main() {
    const int rows = 3;
    const int cols = 3;
    int arr[rows][cols] = {{1, 2, 3}, {4, 5, 6}, {7, 8, 9}};

    return 0;
}
